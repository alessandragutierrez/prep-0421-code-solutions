// convertHoursToMinutes Exercise
function convertHoursToMinutes(hours) {
  return hours * 60;
}

var convertHoursToMinutesResult = convertHoursToMinutes(2);

console.log('convertHoursToMinutes Exercise:', convertHoursToMinutesResult);

